package com.github.PastaLaPate.FPL_IDE.syntax;

public class Word {

    private String word;
    private String color;

    public Word(String word, String color) {
        this.word = word;
        this.color = color;
    }

    public String getWord() {
        return word;
    }

    public String getColor() {
        return color;
    }
}
